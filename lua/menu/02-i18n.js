  // pickLang() -> a key of I18N, following Steam's UI locale (navigator.language).
  function pickLang() {
    try {
      var raw = navigator.language || "en";
      if (I18N[raw]) return raw;
      var p = raw.toLowerCase().split("-")[0];
      if (p === "pt") return "pt-BR";
      if (I18N[p]) return p;
    } catch (e) {}
    return "en";
  }

  // The "Game Updates" strings for the user's language, with a per-call en
  // fallback so a locale without a gu block still renders (in English). The tab
  // code must call this instead of reading I18N.en.gu directly.
  function guStrings() {
    var L = I18N[pickLang()];
    return (L && L.gu) || I18N.en.gu;
  }

  // The "Fixes Menu" strings for the user's language, en fallback.
  function fxStrings() {
    var L = I18N[pickLang()];
    return (L && L.fixes) || I18N.en.fixes;
  }

